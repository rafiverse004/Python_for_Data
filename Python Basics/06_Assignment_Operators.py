#Basic Assignment (=)
x = 10
print(x)


#Add and Assign (+=)
x = 10
x += 5   # x = x + 5

print(x)


#Subtract and Assign (-=)
x = 10
x -= 3   # x = x - 3

print(x)


#Multiply and Assign (*=)
x = 10
x *= 2   # x = x * 2

print(x)


#Divide and Assign (/=)
x = 10
x /= 2   # x = x / 2

print(x)


#Floor Divide and Assign (//=)
x = 10
x //= 3   # x = x // 3

print(x)


#Modulus and Assign (%=)
x = 10
x %= 3   # x = x % 3

print(x)


#Exponent and Assign (**=)
x = 2
x **= 3   # x = x ** 3

print(x)