"""
Variables

A variable stores data in memory so you can use it later.

"""
name = "Rafik"
age = 22


"""
int (Integer)

Whole numbers without decimal points.

"""
age = 22
year = 2026

print(type(age))
print(type(year))


"""
float

Numbers with decimal points.

"""

cgpa = 3.75
price = 99.99

print(type(cgpa))
print(type(price))


"""
str (String)

Text enclosed in quotes.

"""

name = "Rafik"
country = 'Bangladesh'

print(type(name))
print(type(country))


"""
bool (Boolean)

Represents only two values:

True
False

"""

is_student = True
has_job = False

print(type(is_student))
print(type(has_job))


"""
Type Conversion

Used to change one data type into another.

"""

# int to float
num = 25
print(type(num))

num = float(num)
print(type(num))

# int to str
num = 25
print(type(num))

num = str(num)
print(type(num))

# float to int
num = 3.75
print(type(num))

num = int(num)
print(type(num))

# float to str
num = 3.75
print(type(num))

num = str(num)
print(type(num))

# str to int
num = "25"
print(type(num))

num = int(num)
print(type(num))

# str to float
num = "3.75"
print(type(num))

num = float(num)
print(type(num))

