"""
Comparison operators compare two values and return:

True
False

(Data type: bool)

"""

#Equal to (==)
a = 10
b = 10

print(a == b)

#Not equal to (!=)
a = 10
b = 5

print(a != b)

#Greater than (>)
a = 10
b = 5

print(a > b)

#Less Than (<)
a = 5
b = 10

print(a < b)

#Greater Than or Equal To (>=)
a = 10
b = 10

print(a >= b)

#Less Than or Equal To (<=)
a = 5
b = 10

print(a <= b)

