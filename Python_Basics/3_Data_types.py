""" Numeric Types """
# int
age = 21

# float
price = 99.5

# str (string)
name = "Rafik"
city = 'Dhaka'

# bool (Boolean)
is_student = True
is_busy = False

# List (very important) - values can be changed
marks = [10, 20, 30, 40]
names = ["A", "B", "C", "D"]

# Tuple (very important) - values cannot be changed
coords = (23.8, 90.4)

# Dictionary (very important) - Key-value pairs
student = {
    "name": "Rafik",
    "age": 21
}

# Set - Unique values only
number = {1, 2, 3, 3}
output = {1, 2, 3}

