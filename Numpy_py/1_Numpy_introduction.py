import numpy as np

#Python list
my_list = [1, 2, 3, 4]
print(my_list)

#Multiply python list
my_list = my_list * 2
print(my_list)

"""
A numpy array list is far better, far faster, far operational than python list

"""

#Numpy list
array = np.array([1, 2, 3, 4])
print(array)

#Multiply numpy array list
array = array * 2
print(array)